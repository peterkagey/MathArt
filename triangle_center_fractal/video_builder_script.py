from video_builder import VideoBuilder
import random
# --------------------------------------------------
# a^x_1
# (b + c - a)^x_2
# (bc)^x_3
# (b^x_4 + c^x_4)^x_5
param_points = [
    ( 0,  0, 0, 0,  0), # X(2)   => 1                   <--  0
    ( 0, -1, 1, 0,  0), # X(85)  => bc * (b+c-a)^-1     <--  2
    ( 0,  0, 1, 1,  1), # X(321) => bc * (b+c)          <--  3
    ( 1,  0, 0, 2,  1), # X(38)  => a * (b^2+c^2)       <--  6
    ( 2,  1, 0, 0,  0), # X(55)  => a^2 * (b+c-a)       <--  7
    ( 1,  0, 0, 1,  1), # X(37)  => a * (b+c)           <--  8
    ( 1, -1, 0, 0,  0), # X(57)  => a * (b+c-a)^-1      <-- 10
    ( 0, -1, 0, 0,  0), # X(7)   => (b+c-a)^-1          <-- 11
    ( 1,  0, 0, 1, -1), # X(81)  => a * (b+c)^-1        <-- 12
    ( 0,  0, 0, 1, -1), # X(86)  => (b+c)^-1            <-- 13
    ( 0,  0, 0, 2, -1), # X(83)  => (b^2 + c^2)^-1      <-- 14
    ( 0, -1, 2, 0,  0), # X(85)  => (bc)^2 * (b+c-a)^-1 <-- 15
    ( 1,  0, 0, 0,  0), # X(1)  => a                    <-- 16
    ( 2,  0, 0, 2,  1), # X(39) => a^2 * (b^2+c^2)      <-- 17
    ( 3,  1, 0, 0,  0), # X(41) => a^3 * (b+c-a)        <-- 18
    ( 2,  0, 0, 1,  1), # X(42) => a^2 * (b+c)          <-- 19
    ( 2, -1, 0, 0,  0), # X(56) => a^2 * (b+c-a)^-1     <-- 20
    ( 2,  0, 0, 1, -1), # X(58) => a^2 * (b+c)^-1       <-- 21
    ( 1,  0, 0, 2, -1), # X(82) => a * (b^2 + c^2)^-1   <-- 22
    (-2,  0, 0, 0,  0), # X(76) => a^-2                 <-- 23
    # (-1,  0, 0, 0,  0), # X(75)  => a^-1              <--  1
    # ( 0,  0, 0, 1,  1), # X(10)  => b+c               <--  4
    # ( 0,  0, 0, 2,  1), # X(141) => b^2 + c^2         <--  5
    # ( 1,  1, 0, 0,  0), # X(9)   => a * (b+c-a)       <--  9
]
random.seed(919)
random.shuffle(param_points)

VideoBuilder(param_points, frames_per_point=240).make_frames(
  number_of_points=500_000,
  radius=5,
  opacity=0.25,
  print_progress=True
)
